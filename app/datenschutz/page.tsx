function renderBlock(block: string, index: number) {
  if (block.startsWith("# ")) {
    return (
      <header key={index} className="mb-8">
        <p className="text-sm font-semibold uppercase tracking-normal text-brand">{siteOwner.projectName}</p>
        <h1 className="mt-2 text-4xl font-semibold text-ink">{block.replace("# ", "")}</h1>
      </header>
    );
  }

  if (block.startsWith("## ")) {
    return (
      <section key={index} className="mt-8 rounded-lg border border-line bg-white p-6 shadow-soft">
        <h2 className="text-xl font-semibold text-ink">{block.replace("## ", "")}</h2>
      </section>
    );
  }

  if (block.startsWith("* ")) {
    return (
      <ul key={index} className="mt-4 list-disc space-y-2 rounded-lg border border-line bg-white px-8 py-5 text-ink shadow-soft">
        {block.split("\n").map((line) => (
          <li key={line}>{line.replace("* ", "")}</li>
        ))}
      </ul>
    );
  }

  return (
    <div key={index} className="mt-4 rounded-lg border border-line bg-white p-6 leading-7 text-ink shadow-soft">
      {block.split("\n").map((line) => (
        <p key={line} className="mt-2 first:mt-0">
          {renderInline(line)}
        </p>
      ))}
    </div>
  );
}

function renderInline(line: string) {
  const emailLink = line.match(/^\[([^\]]+)\]\(mailto:([^)]+)\)$/);
  if (emailLink) {
    return (
      <a className="text-brand hover:underline" href={`mailto:${emailLink[2]}`}>
        {emailLink[1]}
      </a>
    );
  }

  const mailLine = line.match(/^E-Mail: \[([^\]]+)\]\(mailto:([^)]+)\)$/);
  if (mailLine) {
    return (
      <>
        E-Mail:{" "}
        <a className="text-brand hover:underline" href={`mailto:${mailLine[2]}`}>
          {mailLine[1]}
        </a>
      </>
    );
  }

  if (line.startsWith("**") && line.endsWith("**")) {
    return <strong>{line.slice(2, -2)}</strong>;
  }

  return line;
}
